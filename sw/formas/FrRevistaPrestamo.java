    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        RegistrosPrestamosRevista rev = new RegistrosPrestamosRevista();
        
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy/MM/dd");//formato estandar de fecha
        Calendar calendar = Calendar.getInstance();//llamamos fecha por calentario
        calendar.setTime(new Date());//la recbe
        int calendarTime = Calendar.DAY_OF_MONTH;
        int temp = calendar.get(calendarTime);//para tener el dia
        calendar.set(calendarTime, temp+30);//aumentar el tiempo de fecha de regreso
        Date newDate = calendar.getTime();
       
        String na1 = lblMosNom.getText();
        String na2 = lblMosApeP.getText();
        String na3 = lblMosApeM.getText();
        String na4 = lblMosCurp.getText();
        String na5 = lblMosFecha.getText();
        String na6 = lblMosRev.getText();
        
        String fechaComoCadena = sdf.format(newDate);
        long p = Long.parseLong(lblMosNum.getText());
        
        /**
         * recibe todo lo que hay en las cajas de texto 
         * para poder mandarla al archivo
         */
        rev.llenadoRP(na1, na2, na3, na4, na6,p,na5,fechaComoCadena);
        
        
        limpiar();//limpiamos las cajas de texto
        
    }//GEN-LAST:event_btnRegistrarActionPerformed

