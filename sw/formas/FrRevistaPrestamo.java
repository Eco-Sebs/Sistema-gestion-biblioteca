    private void cmbNomCliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbNomCliActionPerformed
        // TODO add your handling code here:
        int indice=cmbNomCli.getSelectedIndex(); //Recupera el indice empezando desde cero
        Cliente opcion =(Cliente) cmbNomCli.getSelectedItem();/**
         * la lista que tiene de seleccion el combobox de Cliente
         * podra cambiar y se guardara en el elemento 
         */  
        RegistroCliente arc = new RegistroCliente();/**
         * convocamos la funciones de la clase
         * RegistroCliente
         */
        
        if(opcion.getNombre().compareTo("--Selecciona el cliente--")==0){
            //mantenemos las lbl limpia
            lblMosNom.setText("");
        }else{
            //le agregamoms el  nombre a la lbl
            lblMosNom.setText(opcion.getNombre());
        }
        if(opcion.getApellidoP().compareTo("--Selecciona el cliente--")==0){
            //mantenemos las lbl limpia
            lblMosApeP.setText("");
        }else{
            //le agregamoms el Apellido paterno a la lbl 
            lblMosApeP.setText(opcion.getApellidoP());
        }
        if(opcion.getApellidoM().compareTo("--Selecciona el cliente--")==0){
            //mantenemos las lbl limpia
            lblMosApeM.setText("");
        }else{
            //le agregamoms el Apellido materno a la lbl 
            lblMosApeM.setText(opcion.getApellidoM());
        }
        if(opcion.getCurp().compareTo("--Selecciona el cliente--")==0){
            //mantener la lbl limpia
            lblMosCurp.setText("");
        }else{
            //le agregamoms la curp a la lbl 
            lblMosCurp.setText(opcion.getCurp());
        }
        if(opcion.getNombre().compareTo("--Selecciona el cliente--")!=0 && 
                opcion.getApellidoM().compareTo("--Selecciona el cliente--")!=0
                && opcion.getApellidoP().compareTo("--Selecciona el cliente--")!=0 &&
                opcion.getCurp().compareTo("--Selecciona el cliente--")!=0){
            cmbNomCli.removeAllItems();
            llenarListaCliente();
            /**
             * si es difirente se borra la caja de texto y vuelve cargar la lista
             */
        }
        
    }//GEN-LAST:event_cmbNomCliActionPerformed

