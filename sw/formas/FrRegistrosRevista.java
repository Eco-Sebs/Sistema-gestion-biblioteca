    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        RegistroRevista rev = new RegistroRevista();
        
        String na = txtTitulo.getText();
        long na2 = Long.parseLong(txtIsbn.getText());
        String na3 = lblMosdi.getText();
        long p = Long.parseLong(txtNumero.getText());
        int p2 = Integer.parseInt(txtCantidad.getText());
        
        if(rbMensual.isSelected()==true){//por si esta activado
            String na4= "Mensual";
            rev.llenadoR(na, na2, na3, p, p2, na4);//mandamos a la clase
        }else if(rbBimestral.isSelected()==true){//por si esta activado el bimestral
            String na4="Bimestral";
            rev.llenadoR(na, na2, na3, p, p2, na4);//manamos a la clase
        }
        
        limpiar();
        limpiarRb();
        mensaje();
        /**
         * llamar las funciones de limpiar cajas y boton
         */
    }//GEN-LAST:event_btnRegistrarActionPerformed

