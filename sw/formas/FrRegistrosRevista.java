    private void num(JTextField a){//para poder limitar a numeros 
        a.addKeyListener(new KeyAdapter(){
            public void keyTyped(KeyEvent e){
                char c = e.getKeyChar();
                if(!Character.isDigit(c) && c != '.'){
                    e.consume();
                }else if(c== '.' && txtNumero.getText().contains(".")){
                    e.consume();
                }else if(c== '.' && txtCantidad.getText().contains(".")){
                    e.consume();
                }else if(c== '.' && txtIsbn.getText().contains("-")){
                    e.consume();
                }
            }
        });
    }
    
