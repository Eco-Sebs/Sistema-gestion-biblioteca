    private void cmbListaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbListaActionPerformed
        // TODO add your handling code here:
        int indice=cmbLista.getSelectedIndex(); //Recupera el indice empezando desde cero
        Editorial opcion =(Editorial) cmbLista.getSelectedItem();/**
         * la lista que tiene de seleccion el combobox de Editorial
         * podra cambiar y se guardara en el elemento 
         */  
        RegistroEditorial arc = new RegistroEditorial();/**
         * convocamos la funciones de la clase
         * RegistroEditorial
         */
        
        if(opcion.getNombreE().compareTo("--Selecciona el editorial--")==0){
            //para poder mantener limpia la caja de texto
            lblMosdi.setText("");
        }else{//mandamos el nombre a la caja de texto
            lblMosdi.setText(opcion.getNombreE());
        }
        if(opcion.getNombreE().compareTo("--Selecciona el editorial--")!=0){
            cmbLista.removeAllItems();
            llenarListaEditorial();
            /**
             * si es difirente se borra la caja de texto y vuelve cargar la lista
             */
        }
        
    }//GEN-LAST:event_cmbListaActionPerformed

