    public void llenarListaEditorial(){
        RegistroEditorial edi=new RegistroEditorial();/**
         * llamamos la clase RegistroEditorial
         */
        
        LinkedList<Editorial> Lista=edi.listaEditorial();/**
         * generamos una lista ligada de editoriales, que le agregara la lista
         * de la clase 
         */
        for(int i=0; i<Lista.size(); i++){
            //Toma la informacion del toString para poner las etiquetas en la interfaz
            cmbEditorial.addItem(Lista.get(i));//se lo ingresamos como objetos, a combobox
        }
    }
    
    
