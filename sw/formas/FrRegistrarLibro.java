    private void num(JTextField a){//para poder limitar la escritura a numero la caja de texto
        a.addKeyListener(new KeyAdapter(){
            public void keyTyped(KeyEvent e){
                char c = e.getKeyChar();
                if(!Character.isDigit(c) && c != '.'){
                    e.consume();
                }else if(c== '.' && txtCantidad.getText().contains(".")){
                    e.consume();
                }else if(c== '.' && txtIsbn.getText().contains("-")){
                    e.consume();
                }
            }
        });
    }
    
    private void numeros(){
        num(txtCantidad);
        num(txtIsbn);
        /**
         * mandamos la caja de texto a la funcion
         */
    }

