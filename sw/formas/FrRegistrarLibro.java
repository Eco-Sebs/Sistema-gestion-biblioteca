    private void cmbEditorialActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbEditorialActionPerformed
        // TODO add your handling code here:
        
        int indice=cmbEditorial.getSelectedIndex(); //Recupera el indice empezando desde cero
        Editorial opcion =(Editorial) cmbEditorial.getSelectedItem();/**
         * la lista que tiene de seleccion el combobox de Editorial
         * podra cambiar y se guardara en el elemento 
         */  
        RegistroEditorial arc = new RegistroEditorial();/**
         * convocamos la funciones de la clase
         * RegistroEditorial
         */
        
        if(opcion.getNombreE().compareTo("--Selecciona el editorial--")==0){//limpiamos caja de texto
            lblMosedi.setText("");
        }else{
            lblMosedi.setText(opcion.getNombreE());
        }
        if(opcion.getNombreE().compareTo("--Selecciona el editorial--")!=0){
            cmbEditorial.removeAllItems();
            llenarListaEditorial();
            /**
             * si es difirente se borra la caja de texto y vuelve cargar la lista
             */
        }
        
    }//GEN-LAST:event_cmbEditorialActionPerformed

