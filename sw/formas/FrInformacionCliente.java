    private void cmbClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbClienteActionPerformed
        // TODO add your handling code here:
        
        int indice=cmbCliente.getSelectedIndex(); //Recupera el indice empezando desde cero
        Cliente opcion =(Cliente) cmbCliente.getSelectedItem();/**
         * la lista que tiene de seleccion el combobox de Cliente
         * podra cambiar y se guardara en el elemento 
         */  
        RegistroCliente arc = new RegistroCliente();/**
         * convocamos la funciones de la clase
         * RegistroCliente
         */
        
        if(opcion.getNombre().compareTo("--Selecciona el cliente--")==0){
            //mantenemos las caja limpia
            txtNombre.setText("");
        }else{
            //le agregamoms el  nombre a la caja de texto
            txtNombre.setText(opcion.getNombre());
        }
        if(opcion.getApellidoP().compareTo("--Selecciona el cliente--")==0){
            //mantenemos las caja limpia
            txtApellidoPa.setText("");
        }else{
            //le agregamoms el Apellido paterno a la caja de texto
            txtApellidoPa.setText(opcion.getApellidoP());
        }
        if(opcion.getApellidoM().compareTo("--Selecciona el cliente--")==0){
            //mantenemos las caja limpia
            txtApellidoMa.setText("");
        }else{
            //le agregamoms el Apellido materno a la caja de texto
            txtApellidoMa.setText(opcion.getApellidoM());
        }
        if(opcion.getNombre().compareTo("--Selecciona el cliente--")!=0 && 
                opcion.getApellidoM().compareTo("--Selecciona el cliente--")!=0
                && opcion.getApellidoP().compareTo("--Selecciona el cliente--")!=0 &&
                opcion.getCurp().compareTo("--Selecciona el cliente--")!=0){
            cmbCliente.removeAllItems();
            llenarListaCliente();
            /**
             * si es difirente se borra la caja de texto y vuelve cargar la lista
             */
        }
    }//GEN-LAST:event_cmbClienteActionPerformed

