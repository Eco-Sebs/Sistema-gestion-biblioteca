    private void llenarListaCliente(){
        RegistroCliente cli=new RegistroCliente();/**
         * llamamos la clase RegistroCliente
         */
        
        LinkedList<Cliente> Lista=cli.listaCliente();/**
         * generamos una lista ligada de Cliente, que le agregara la lista
         * de la clase RegistroCliente
         */
        for(int i=0; i<Lista.size(); i++){
            //Toma la informacion del toString para poner las etiquetas en la interfaz
            cmbCliente.addItem(Lista.get(i));//se lo ingresamos como objetos, a combobox
        }
    }
    
