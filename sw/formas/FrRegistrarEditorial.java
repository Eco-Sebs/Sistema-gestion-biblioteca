    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        String na = txtNombreE.getText();
        String na2 = txtRFC.getText();
        String na3 = txtDireccion.getText();
        long na4 = Long.parseLong(txtTelefonoC.getText());
        String na5 = txtNombreR.getText();
        String na6 = txtCorreoE.getText();
        
        RegistroEditorial reg = new RegistroEditorial();
        
        /**
         * guardamos la informacion de la caja de texto
         * en sus respectivas variables para poder enviarla 
         * a la funcion
         */
        
        reg.llenadoE(na, na2, na3, na4, na5, na6);
        
        limpiar();//limpiar las cajas de texto
        mensaje();
    }//GEN-LAST:event_btnRegistrarActionPerformed

