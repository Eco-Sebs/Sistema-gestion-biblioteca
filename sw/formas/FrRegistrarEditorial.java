    private void num(JTextField a){//para poder limitar a solo numeros
        a.addKeyListener(new KeyAdapter(){
            public void keyTyped(KeyEvent e){
                char c = e.getKeyChar();
                if(!Character.isDigit(c) && c != '.'){
                    e.consume();
                }else if(c== '.' && txtTelefonoC.getText().contains(".")){
                    e.consume();
                }
            }
        });
    }
    
    private void numeros(){
        num(txtTelefonoC);//la caja de texto de numero 
    }
    
